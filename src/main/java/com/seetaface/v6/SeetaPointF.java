package com.seetaface.v6;

/**
 * 人脸特征点
 */
public class SeetaPointF {
    /**
     * 人脸特征点横坐标
     */
    public double x;
    /**
     * 人脸特征点纵坐标
     */
    public double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
